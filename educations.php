                        <h3 class="text-center">
                            R.I.S.E. And Shoot For the Moon: Four Pillars of Success<br>
                            <span class="name">Anita Adams</span>
                        </h3>
                        <p class="lead text-center">This workshop is for those who want to take their success in
                            business and in life to new heights.</p>
                        <p><b> Pillar One: Resilience</b><br />
                            The number one reason why people don’t succeed at achieving their goals is because they
                            experience failure and/or rejection and give up. In this section we’ll explore how to deal
                            with failure and rejection in a way that will help participants move forward. </p>
                        <p><b> Pillar Two: Imagination</b><br />
                            If you want to be successful, you must dare to dream big. Imagination is the starting point
                            of success. Every goal starts off as something imagined and in order to achieve great
                            success you must first imagine it. Here we’ll explore how to imagine success into reality
                        </p>
                        <p><b> Pillar Three: Self-belief</b><br />
                            This is the most important pillar as without self belief no amount of imagining, resilience
                            or endurance will get you to where you want to be. Here we’ll explore some techniques that
                            will boost that self belief so you become unstoppable.
                        </p>
                        <p><b> Pillar Four: Endurance</b><br />
                            A cousin of resilience, but endurance is more about being committed to the long run than
                            bouncing back from setbacks. In this section we’ll explore ways to keep your mind and spirit
                            well fuelled for going the distance.
                        </p>
                        <p>Learning Objectives:</p>
                        <ul>
                            <li>To learn the four essential pillars of success that will help participants reach new
                                heights of success in business and in life.</li>
                            <li>To understand that rejection and failure are inevitable when pursuing big dreams and to
                                identify ways to move past those set backs and stay on course</li>
                            <li>To learn how to turn your imagination into a powerful tool that will help you achieve
                                your wildest dreams.</li>
                            <li>To learn a few simple techniques that will help strengthen your self belief and keep you
                                focused on the long game. </li>
                        </ul>
                        <p>Anita Adams is the Founder and Executive Director of a National organization that has been
                            promoting Canadian film and talent since 2003. To date, she has promoted over 700 feature
                            films and countless talent and has partnered with some of the largest film institutions in
                            Canada, including the National Film Board of Canada, Cineplex Entertainment, and the
                            Directors Guild of Canada, to name a few. In 2013, Anita packed up her family and moved to
                            the South of France for a year. Many questioned how she could build her Canadian film
                            business from Europe, but Anita didn’t allow the limiting beliefs of others to prevent her
                            from living the life she wanted. That year abroad turned out to be one that propelled her
                            business in a way she never imagined and altered the trajectory of her life, which lead her
                            to launch a second business a few years later. Now Anita is the owner and operator of two
                            successful home-based / work from anywhere businesses and is currently planning another year
                            abroad that will take her and her family to India this September.</p>
                        <p>Her experience in building two businesses while creating and living a life she loves, has
