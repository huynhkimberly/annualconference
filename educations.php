          <h3 class="text-center"> 
              How to Create a Resilient Dynamic Workplace Culture<br>
              <span class="name">Emi Garzitto</span>
            </h3>
              <p class="lead">Strong independent resilient communities do not happen by accident.  </p> 
              <p>Current research shows children and adults are no longer getting the practice, skills 
                and rehearsal required to train the brain to manage strong feelings, take care of peer 
                to peer conflict with independence and respect, take risks, solve problems and manage 
                failure. All of these skills are critical in order to create a resilient, dynamic 
                workplace culture.</p> 
              <p>In this workshop, we will: </p>
              <ul>
                <li>Look at current research that suggests including movement, risk and unpredictability</li>
                <li>Identify self-care strategies and skills so that staff feel able and empowered to manage 
                  stress, unpredictability and conflict. </li>
                <li>Provide practical strategies, routines and conversations that will support resilience, 
                  independence and problem solving skills in the workplace.</li>
              </ul> 
              <p>For the last 28 years, Dr. Garzitto has been a passionate advocate for helping communities 
                learn the skills for getting along. Her consulting practice, Creative Edge, provides corporate 
                and personal conflict coaching.  Her research and writing explores challenges and opportunities 
                that conflict bring both personally and collectively. Dr. Garzitto is author of Your Beautiful 
                Trauma, which explores the habits and skills that can help individuals adapt and thrive through 
                personal crises.</p> 
              <p>For more information on Dr. Garzitto please visit <a href="https://www.creativeedgeconsulting.com/" target=_blank>creativeedgeconsulting.com</a></p> 
